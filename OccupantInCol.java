public class OccupantInCol 
{ 
 private Object occupant; 
 private int col; 
 
 public OccupantInCol(Object p, int c) 
 { 
 occupant = p; 
 col = c; 
 } 
 
 public Object getOccupant() 
 { 
 return occupant; 
 } 
 
 public int getCol() 
 { 
 return col; 
 } 
 
 public void setOccupant(Object p) 
 { 
 occupant = p; 
 } 
 
}
